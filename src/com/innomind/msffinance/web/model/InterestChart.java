package com.innomind.msffinance.web.model;

public class InterestChart {
	private String receivedAmount;
	private String notReceivedAmount;
	
	public String getReceivedAmount() {
		return receivedAmount;
	}
	public void setReceivedAmount(String receivedAmount) {
		this.receivedAmount = receivedAmount;
	}
	public String getNotReceivedAmount() {
		return notReceivedAmount;
	}
	public void setNotReceivedAmount(String notReceivedAmount) {
		this.notReceivedAmount = notReceivedAmount;
	}
	
}
